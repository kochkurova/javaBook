public class GalToLit {
    public static void main (String args [] ) {

        // программа перевода галлонов в литры

        double gallons;
        double liters;
        gallons = 10;
        liters = gallons * 3.7854;
        System.out.println("Десяти галлонам соответствует " + liters + " литра");


        // мили в метры
        double miles;
        double kilometers;
        miles = 15;
        kilometers = miles * 0.6213;

        System.out.println("15 километрам соответствует " + kilometers + " километров");


    }










}
